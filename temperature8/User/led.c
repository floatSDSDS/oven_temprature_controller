#include "led.h"
#include "Sys.h"

/******************************************************************************
  * 函数：  LED_Init()
  * 描述：  LED初始化
  * 输入值：无
  *	返回值：无
  ******************************************************************************/
void LED_Init()
{

	SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOF);//使能GPIOF
	LED_GPIOF_OUT();
}

/******************************************************************************
  * 函数：  LED_On()
  * 描述：  灯亮
  * 输入值：无
  *	返回值：无
  ******************************************************************************/
void LED_On()
{
	LED0_ON();
	DelayMs(125);
}


/******************************************************************************
  * 函数：  LED_Off()
  * 描述：  灯灭
  * 输入值：无
  *	返回值：无
  ******************************************************************************/
void LED_Off()
{
	LED0_OFF();
	DelayMs(125);
}


